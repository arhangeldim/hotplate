package gmc.hotplate.entities;


public class Step {


    private int id;
    private String description;
    private int time;

    public Step() {

    }

    public Step(int id, String description, int seconds) {
        this.id = id;
        this.description = description;
        this.time = seconds;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

}
